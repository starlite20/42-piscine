#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	hex_to_char(unsigned char c)
{
	char	*hex;

	hex = "0123456789abcdef";
	write(1, "\\", 1);
	ft_putchar(hex[c / 16]);
	ft_putchar(hex[c % 16]);
}

void	ft_putstr_non_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 32 || str[i] == 127)
			hex_to_char(str[i]);
		else
			ft_putchar(str[i]);
		i++;
	}
}
